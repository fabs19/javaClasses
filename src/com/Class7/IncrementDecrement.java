package com.Class7;

public class IncrementDecrement {

	public static void main(String[] args) {
		//increment & decrement operators can be used only with variables

		int a=10;
		
		a=a+1; //assignment 
		a+=1; //short hand
		
		
		int b=10;
		b-=1; //9
		b--; //decrement
		//System.out.println(b);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
